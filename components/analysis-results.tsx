"use client"

import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card"
import { Button } from "@/components/ui/button"
import { Badge } from "@/components/ui/badge"
import { Download, CheckCircle, AlertTriangle, Loader2 } from "lucide-react"

interface AnalysisResult {
  prediction: "positive" | "negative"
  confidence: number
  model: string
  gradCamUrl: string
  timestamp: string
}

interface AnalysisResultsProps {
  result: AnalysisResult | null
  isAnalyzing: boolean
  originalImage: string | null
}

export function AnalysisResults({ result, isAnalyzing, originalImage }: AnalysisResultsProps) {
  const downloadReport = () => {
    if (!result) return

    const report = `
KAPKIVOC TRACER - LUNG CANCER DETECTION REPORT
================================================

Date: ${new Date(result.timestamp).toLocaleString()}
Model Used: ${result.model.toUpperCase()}

ANALYSIS RESULTS
----------------
Prediction: ${result.prediction === "positive" ? "POSITIVE - Cancer Indicators Detected" : "NEGATIVE - No Cancer Indicators Detected"}
Confidence: ${(result.confidence * 100).toFixed(1)}%

DISCLAIMER
----------
This analysis is for educational and practice purposes only.
It should NOT be used for actual medical diagnosis.
Always consult qualified healthcare professionals for medical decisions.

================================================
Generated by Kapkivoc Tracer
    `.trim()

    const blob = new Blob([report], { type: "text/plain" })
    const url = URL.createObjectURL(blob)
    const link = document.createElement("a")
    link.href = url
    link.download = `kapkivoc-report-${Date.now()}.txt`
    document.body.appendChild(link)
    link.click()
    document.body.removeChild(link)
    URL.revokeObjectURL(url)
  }

  if (isAnalyzing) {
    return (
      <Card className="border-border">
        <CardHeader>
          <CardTitle>Analysis Results</CardTitle>
          <CardDescription>Processing your image...</CardDescription>
        </CardHeader>
        <CardContent className="flex flex-col items-center justify-center py-12">
          <Loader2 className="h-12 w-12 animate-spin text-primary mb-4" />
          <p className="text-muted-foreground">Analyzing image with AI model...</p>
          <p className="text-sm text-muted-foreground mt-2">This may take a few seconds</p>
        </CardContent>
      </Card>
    )
  }

  if (!result) {
    return (
      <Card className="border-border">
        <CardHeader>
          <CardTitle>Analysis Results</CardTitle>
          <CardDescription>Upload and analyze an image to see results</CardDescription>
        </CardHeader>
        <CardContent className="flex flex-col items-center justify-center py-12 text-center">
          <div className="h-16 w-16 rounded-full bg-muted flex items-center justify-center mb-4">
            <CheckCircle className="h-8 w-8 text-muted-foreground" />
          </div>
          <p className="text-muted-foreground">Results will appear here after analysis</p>
        </CardContent>
      </Card>
    )
  }

  const isPositive = result.prediction === "positive"

  return (
    <Card className="border-border">
      <CardHeader>
        <CardTitle className="flex items-center justify-between">
          Analysis Results
          <Badge
            variant={isPositive ? "destructive" : "default"}
            className={!isPositive ? "bg-green-600 hover:bg-green-700" : ""}
          >
            {isPositive ? "Positive" : "Negative"}
          </Badge>
        </CardTitle>
        <CardDescription>Analyzed on {new Date(result.timestamp).toLocaleString()}</CardDescription>
      </CardHeader>
      <CardContent className="space-y-6">
        {/* Prediction Card */}
        <div className={`p-4 rounded-lg ${isPositive ? "bg-destructive/10" : "bg-green-500/10"}`}>
          <div className="flex items-start gap-3">
            {isPositive ? (
              <AlertTriangle className="h-6 w-6 text-destructive flex-shrink-0" />
            ) : (
              <CheckCircle className="h-6 w-6 text-green-600 flex-shrink-0" />
            )}
            <div>
              <h3 className={`font-semibold ${isPositive ? "text-destructive" : "text-green-600"}`}>
                {isPositive ? "Cancer Indicators Detected" : "No Cancer Indicators Detected"}
              </h3>
              <p className="text-sm text-muted-foreground mt-1">
                {isPositive
                  ? "The AI model has identified potential cancer indicators in the uploaded image. Please consult a medical professional for proper diagnosis."
                  : "The AI model did not detect cancer indicators in the uploaded image. However, this is not a medical diagnosis."}
              </p>
            </div>
          </div>
        </div>

        {/* Stats */}
        <div className="grid grid-cols-2 gap-4">
          <div className="p-4 rounded-lg bg-muted">
            <p className="text-sm text-muted-foreground">Confidence</p>
            <p className="text-2xl font-bold text-foreground">{(result.confidence * 100).toFixed(1)}%</p>
          </div>
          <div className="p-4 rounded-lg bg-muted">
            <p className="text-sm text-muted-foreground">Model Used</p>
            <p className="text-2xl font-bold text-foreground capitalize">{result.model}</p>
          </div>
        </div>

        <div className="space-y-2">
          <h4 className="font-medium text-foreground">Grad-CAM Visualization</h4>
          <p className="text-sm text-muted-foreground">
            Highlighted areas indicate regions the model focused on during analysis.
          </p>
          <div className="relative rounded-lg overflow-hidden border border-border">
            {result.gradCamUrl ? (
              <img src={result.gradCamUrl || "/placeholder.svg"} alt="Grad-CAM visualization" className="w-full" />
            ) : (
              <div className="w-full h-48 bg-muted flex items-center justify-center">
                <p className="text-muted-foreground">Grad-CAM visualization not available</p>
              </div>
            )}
          </div>
        </div>

        {/* Download Button */}
        <Button onClick={downloadReport} className="w-full gap-2">
          <Download className="h-4 w-4" />
          Download Report
        </Button>
      </CardContent>
    </Card>
  )
}
